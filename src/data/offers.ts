export type OfferStatus = 
  | "draft"
  | "sent"
  | "accepted"
  | "rejected"
  | "in_progress"
  | "delivered"
  | "completed"
  | "disputed"
  | "cancelled";

export type OfferCategory = 
  | "FYP / Thesis"
  | "Research Paper Support"
  | "Data Analysis"
  | "Coding / App Dev"
  | "UI/Design"
  | "Presentation/Poster"
  | "Other";

export interface Offer {
  id: string;
  title: string;
  category: OfferCategory;
  description: string;
  requiredSkills: string[];
  budgetType: "fixed" | "hourly";
  budget: number;
  deadline: string;
  visibility: "private" | "public";
  senderId: string;
  senderName: string;
  senderAvatar: string;
  receiverId?: string;
  receiverName?: string;
  receiverAvatar?: string;
  status: OfferStatus;
  createdAt: string;
  acceptedBidId?: string;
}

export interface Bid {
  id: string;
  offerId: string;
  bidderId: string;
  bidderName: string;
  bidderAvatar: string;
  proposedPrice: number;
  deliveryTime: string;
  message: string;
  portfolioLink?: string;
  status: "pending" | "accepted" | "declined";
  createdAt: string;
}

export interface Transaction {
  id: string;
  offerId: string;
  offerTitle: string;
  category: OfferCategory;
  grossAmount: number;
  commissionPercent: number;
  commissionAmount: number;
  netPayout: number;
  providerId: string;
  providerName: string;
  clientId: string;
  clientName: string;
  completedAt: string;
}

export interface ToolEvent {
  id: string;
  toolId: string;
  toolName: string;
  userId: string;
  eventType: "viewed" | "clicked_buy" | "bundle_interest";
  timestamp: string;
}

export interface Notification {
  id: string;
  type: "match_request" | "offer_received" | "bid_received" | "offer_accepted" | "offer_rejected";
  title: string;
  message: string;
  read: boolean;
  createdAt: string;
  link?: string;
}

